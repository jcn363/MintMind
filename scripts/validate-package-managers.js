const fs = require('fs');
const path = require('path');

/**
 * Script de validación para asegurar que no existan archivos de lock
 * de otros gestores de paquetes en el proyecto MintMind.
 * Este script es compatible con Bun y solo valida la ausencia
 * de archivos de lock de otros gestores.
 */

const lockFilesToCheck = [
  'yarn.lock',
  'package-lock.json',
  'pnpm-lock.yaml',
  'pnpm-lock.yml'
];

const projectRoot = path.resolve(__dirname, '..');

console.log('Verificando archivos de lock de otros gestores de paquetes...');

let hasErrors = false;

for (const lockFile of lockFilesToCheck) {
  const filePath = path.join(projectRoot, lockFile);
  if (fs.existsSync(filePath)) {
    console.error(`❌ Encontrado archivo de lock de otro gestor: ${lockFile}`);
    hasErrors = true;
  }
}

if (hasErrors) {
  console.error('El proyecto debe usar solo Bun como gestor de paquetes.');
  process.exit(1);
} else {
  console.log('✅ No se encontraron archivos de lock de otros gestores.');
}